#Write a Python program to print a formatted string using print() and f-string.

# Variables to include in the formatted string
name = "Anjali"
age = 25
city = "Chennai"

# Using print() with concatenation
print("Name: " + name + ", Age: " + str(age) + ", City: " + city)

# Using print() with f-string formatting (preferred way)
print(f"Name: {name}, Age: {age}, City: {city}")
